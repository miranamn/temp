package functions;

public class FunctionPointT {
    private Double X;
    private Double Y;

    public FunctionPointT(double x, double y)
    {
        this.X = x;
        this.Y = y;
    }

    public Double getX()
    {
        return X;
    }

    public Double getY()
    {
        return Y;
    }

    public void setX(double x){
        this.X = x;
    }

    public void setY(double y)
    {
        this.Y = y;
    }


}
